
public class PrintHelper {

	public void pl(String s) {
		System.out.println(s);
	}
	
	public void pl(int s) {
		System.out.println(s);
	}
	
	public void pl(long s) {
		System.out.println(s);
	}
	
	public void p(String s) {
		System.out.print(s+" ");
	}
	
	public void p(int s) {
		System.out.print(s+" ");
	}
	
	public void p(long s) {
		System.out.print(s+" ");
	}
	
	public void p_break() {
		System.out.print("\n------------------- *** ---------------------\n");
	}
}
